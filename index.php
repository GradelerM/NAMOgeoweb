                <h1>Présentation</h1>
                <p>
                    Le projet RIVAGE, coordonné par le Cirad, propose de mettre en place un dispositif 
                    sociotechnique d'accompagnement de la transition écologique de l'agriculture guadeloupéenne. 
                    Ce dispositif vise à accompagner les agriculteurs d'un territoire dans l'adoption de pratiques 
                    agro-écologiques, et à faciliter l'intervention des acteurs locaux dans la gestion de l'environnement. 
                    Il s'agira de mieux connaître l'activité agricole et ses déterminants à l'échelle individuelle 
                    et à celle du territoire ; d'apporter des connaissances sur le fonctionnement du milieu physique 
                    et d'établir un diagnostic de l'impact des pratiques sur les différents compartiments sols, nappes 
                    et rivières à l'échelle du bassin versant.
                </p>
